package Day8;
import java.util.*;

  
public class MaxMin {
	
	
	    public static void main(String args[])
	    {
	        
	        ArrayList<Integer> arr = new ArrayList<Integer>();
	  
	        
	        arr.add(10);
	        arr.add(20);
	        arr.add(45);
	        arr.add(55);
	  
	        System.out.println("Maximum is : " + Collections.max(arr));
	  
	        
	        System.out.println("Minimum is : " + Collections.min(arr));
	    }
	}

